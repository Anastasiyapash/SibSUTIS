;-------------------------------------------------------------
; Пример арифметических операций над двоичными целыми числами.
;-------------------------------------------------------------
masm
model	small
stack	256
.data
a	db	254                     ;FEh
b	dw	0
;-----------------------------------------------------------
x	db      227
y	db      27
;-----------------------------------------------------------
c	dw      340
d	dw      420
;-----------------------------------------------------------
e	dw	680
f	db      60
;-----------------------------------------------------------
.code	;сегмент кода
main:					;точка входа в программу                                 	
	mov	ax,@data                ;загрузка сегментного регистра
	mov	ds,ax                   ;настройка регистра DS     
	                                ;	на адрес сегмента данных
;-----------------------------------------------------------
;...
	xor	ax,ax                   ;очистка регистра AX
	add	al,17                   ;AL=17(11h)
	add	a8l,a                    ;AL=17+254=271(10Fh) должно быть
	jnc	m1	;если нет переноса, то перейти на m1
	adc	ah,0	;в ax сумма с учетом переноса
m1: 
	mov	b,ax			;запомнить результат
;-----------------------------------------------------------
	xor	ax,ax
	mov	al,x
	sub	al,y
	jnc	m2
	cbw
	neg	ax
m2:
	mov	c,ax
;-----------------------------------------------------------
;...
;-----------------------------------------------------------
;Вставка сложения x + y
	mov ax,x
	add ax,y	
;-----------------------------------------------------------
;Вставка вычитания x - y
	mov ax,x
	sub ax,y
;-----------------------------------------------------------
;Вставка умножения c x d
	mov ax,d
	mul c
;-----------------------------------------------------------
;Вставка деления   e/f
	mov ax,f
	div e
;-----------------------------------------------------------
exit:
	mov	ax,4c00h	;стандартный выход
	int	21h
end main			;конец программы

